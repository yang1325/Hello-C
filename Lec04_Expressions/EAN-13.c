#include <stdio.h>

int main(void){
    int a,b,c,d,e,f;
    scanf("%d %d %d",&a,&b,&c);
    d=((b/100000)%10)*3+((b/10000)%10)*1+((b/1000)%10)*3+((b/100)%10)*1+((b/10)%10)*3+(b%10)*1;
    e=((c/10000)%10)*3+((c/1000)%10)*1+((c/100)%10)*3+((c/10)%10)*1+(c%10)*3;
    a=a+d+e;
    f=(10-(a%10))%10;
    printf("%d",f);
    return 0;
}
